// =============================================================================
// tb_spline_eval.sv
//
// Testbench for spline_eval.sv.
// Models the exact signal path that exists in elementconn:
//   - Drives cmdstb and clock
//   - Provides BRAM models with READ_LATENCY=2 (matching aligned_ram.v)
//   - Logs output to CSV for comparison with Python reference
//
// RUN WITH ICARUS VERILOG:
//   1. Generate BRAM init files:
//        python spline_coeff_pack.py
//   2. Compile and simulate:
//        iverilog -g2012 -o tb tb_spline_eval.sv spline_eval.sv
//        vvp tb
//   3. Compare output:
//        python compare_output.py   (see end of spline_coeff_pack.py)
//
// NOTE: spline_eval.sv uses reg_delay1, which is a QubiC gateware primitive.
// For standalone simulation outside the QubiC build system, the simple model
// at the bottom of this file is used instead (controlled by `define STANDALONE).
//
// =============================================================================

`timescale 1ns/1ps
`define STANDALONE   // Use simple reg_delay1 model below; remove when
                     // compiling inside the QubiC gateware build system

// Simple reg_delay1 model for standalone simulation
// In the real gateware this is a primitive in gateware/bits/
`ifdef STANDALONE
module reg_delay1 #(parameter DW=1, parameter LEN=1)(
    input              clk,
    input              gate,
    input  [DW-1:0]    din,
    output [DW-1:0]    dout,
    input              reset
);
    reg [DW-1:0] sr [0:LEN-1];
    integer k;
    always @(posedge clk) begin
        if (reset) begin
            for (k=0; k<LEN; k=k+1) sr[k] <= '0;
        end else if (gate) begin
            sr[0] <= din;
            for (k=1; k<LEN; k=k+1) sr[k] <= sr[k-1];
        end
    end
    assign dout = sr[LEN-1];
endmodule
`endif

// ─────────────────────────────────────────────────────────────────────────────
module tb_spline_eval;

localparam SEG_ADDRW = 12;
localparam CLK_PERIOD = 1;       // 1 ns = 1 GSPS
localparam BRAM_DEPTH = 4096;
localparam OUT_DELAY  = 28;

// DUT ports
reg                    clk       = 0;
reg                    cmdstb    = 0;
reg                    reset     = 1;
reg  [SEG_ADDRW-1:0]   coeff_base = 0;
reg  [SEG_ADDRW-1:0]   n_segs     = 0;

wire [SEG_ADDRW-1:0]   coeff_addr;
wire [SEG_ADDRW-1:0]   width_addr;
wire [31:0]            envdata_out;
wire                   valid_out;
wire                   busy_out;

// BRAM models with READ_LATENCY=2 (matches aligned_ram.v default)
reg [127:0] coeff_bram [0:BRAM_DEPTH-1];
reg [31:0]  width_bram [0:BRAM_DEPTH-1];
reg [127:0] coeff_data_r0 = 0, coeff_data_out = 0;
reg [31:0]  width_data_r0 = 0, width_data_out = 0;

initial begin
    // Load BRAM from files generated by spline_coeff_pack.py
    // Run: python spline_coeff_pack.py  before simulating
    $readmemh("coeff_init.mem", coeff_bram);
    $readmemh("width_init.mem", width_bram);
end

// Synchronous BRAM read, READ_LATENCY=2
always @(posedge clk) begin
    coeff_data_r0  <= coeff_bram[coeff_addr];
    coeff_data_out <= coeff_data_r0;
    width_data_r0  <= width_bram[width_addr];
    width_data_out <= width_data_r0;
end

// DUT
spline_eval #(
    .SEG_ADDRW (SEG_ADDRW),
    
    .OUT_DELAY (OUT_DELAY)
) dut (
    .clk        (clk),
    .cmdstb     (cmdstb),
    .reset      (reset),
    .coeff_base (coeff_base),
    .n_segs     (n_segs),
    .coeff_addr (coeff_addr),
    .coeff_data (coeff_data_out),
    .width_addr (width_addr),
    .width_data (width_data_out),
    .envdata_out(envdata_out),
    .valid_out  (valid_out),
    .busy_out   (busy_out)
);

// Clock
always #(CLK_PERIOD/2.0) clk = ~clk;

// Output log
integer fout;
integer sample_cnt = 0;

// Q1.15 to float for display
function real q115(input signed [15:0] v);
    q115 = $itor(v) / 32768.0;
endfunction

// Test sequence
initial begin
    $display("=== tb_spline_eval ===");
    $display("Q0 readout cos_edge_square: twidth=2us, amp=0.2408, 40 segments");
    fout = $fopen("spline_eval_output.csv", "w");
    $fwrite(fout, "cycle,valid,I_q115,Q_q115,I_float,Q_float\n");

    repeat(5) @(posedge clk);
    reset = 0;

    // Configure for Q0 readout: 40 segments at base address 0
    @(posedge clk);
    n_segs     = 40;
    coeff_base = 0;

    // Assert cmdstb for one cycle
    @(posedge clk);
    cmdstb = 1;
    @(posedge clk);
    cmdstb = 0;

    $display("t=%0t: cmdstb asserted", $time);

    // Run for pulse duration + pipeline flush
    // 2000 cycles (2µs) + 35 pipeline cycles + some margin
    repeat(2060) @(posedge clk);

    $fclose(fout);
    $display("Done. %0d samples written to spline_eval_output.csv", sample_cnt);
    $display("Run: python spline_coeff_pack.py to generate reference, then compare.");
    $finish;
end

// Capture output samples
always @(posedge clk) begin
    if (valid_out) begin
        $fwrite(fout, "%0d,1,%0d,%0d,%.6f,%.6f\n",
            sample_cnt,
            $signed(envdata_out[31:16]),
            $signed(envdata_out[15:0]),
            q115($signed(envdata_out[31:16])),
            q115($signed(envdata_out[15:0]))
        );
        sample_cnt = sample_cnt + 1;
    end
end

// Waveform dump
initial begin
    $dumpfile("tb_spline_eval.vcd");
    $dumpvars(0, tb_spline_eval);
end

// Timeout
initial begin
    #100_000;
    $display("TIMEOUT");
    $finish;
end

endmodule
